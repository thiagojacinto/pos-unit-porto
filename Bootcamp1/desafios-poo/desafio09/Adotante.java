package desafio09;

public class Adotante {
	private int id;
	private String name;
	private String telefoneFixo;
	private String telefoneCelular;
	private String email;
	private Endereco endereco;
	private boolean impedimento;
	private String motivoImpedimento;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the telefoneFixo
	 */
	public String getTelefoneFixo() {
		return telefoneFixo;
	}
	/**
	 * @param telefoneFixo the telefoneFixo to set
	 */
	public void setTelefoneFixo(String telefoneFixo) {
		this.telefoneFixo = telefoneFixo;
	}
	/**
	 * @return the telefoneCelular
	 */
	public String getTelefoneCelular() {
		return telefoneCelular;
	}
	/**
	 * @param telefoneCelular the telefoneCelular to set
	 */
	public void setTelefoneCelular(String telefoneCelular) {
		this.telefoneCelular = telefoneCelular;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the endereco
	 */
	public Endereco getEndereco() {
		return endereco;
	}
	/**
	 * @param endereco the endereco to set
	 */
	public void setEndereco(Endereco endereco) {
		this.endereco = endereco;
	}
	/**
	 * @return the impedimento
	 */
	public boolean isImpedimento() {
		return impedimento;
	}
	/**
	 * @param impedimento the impedimento to set
	 */
	public void setImpedimento(boolean impedimento) {
		this.impedimento = impedimento;
	}
	/**
	 * @return the motivoImpedimento
	 */
	public String getMotivoImpedimento() {
		return motivoImpedimento;
	}
	/**
	 * @param motivoImpedimento the motivoImpedimento to set
	 */
	public void setMotivoImpedimento(String motivoImpedimento) {
		this.motivoImpedimento = motivoImpedimento;
	}
	
}
